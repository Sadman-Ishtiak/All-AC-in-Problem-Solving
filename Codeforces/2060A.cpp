using namespace std;

void solve(){
    int a, b, d, e;
    cin >> a >> b >> d >> e;
    int ans = 0;
    for (int i = -1000; i < 1001; i++) {
        int fb = 0;
        if(a + b == i) fb++;
        if(b + i == d) fb++;
        if(i + d == e) fb++;
        ans = max(ans, fb);
    }
    cout << ans << endl;
}
int32_t main() {
    int t = 1;
    cin >> t;
    while(t--) solve();
    return 0;
}