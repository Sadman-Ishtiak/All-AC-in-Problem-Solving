void solve(){
    int n, b, x, y;
    cin >> n >> b >> x >> y;
    int ans = 0;
    int curritem = 0;
    int i = 1;
    while (i <= n) {
        if(curritem + x <= b){
            curritem += x;
        } else curritem -= y;
        ans += curritem;
        i++;
    }
    cout << ans << endl;
}
