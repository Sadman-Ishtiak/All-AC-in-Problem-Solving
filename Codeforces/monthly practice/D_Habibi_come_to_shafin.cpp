using namespace std;

void solve(){
    int n, k;
    cin >> n >> k;
    vector<int>a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    map<int, int>prefix_sums;
    int ans = 0, current_sum = 0;
    for (int i = 0; i < n; i++) {
        current_sum += a[i];
        if(current_sum == k) ans++;

        if(prefix_sums.find(current_sum - k) != prefix_sums.end()) {
            ans += prefix_sums[current_sum - k];
        }
        prefix_sums[current_sum]++;
    }
    cout << ans << endl;
}
int32_t main() {
    int t = 1;
    cin >> t;
    while(t--) solve();
    return 0;
}