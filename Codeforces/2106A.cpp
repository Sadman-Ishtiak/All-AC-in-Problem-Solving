void solve(){
    int n, ans = 0;
    cin >> n;
    string s;
    cin >> s;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            if(i == j) ans += ((s[j] == '0')?1:0);
            else ans += ((s[j] == '1')?1:0);
        }
    }
    cout << ans << endl;
}
